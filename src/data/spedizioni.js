export const CI_BASE = {
  'IT10006': 4.2, 'IT10025': 3.8, 'IT12447': 5.1, 'IT16481': 4.6,
  'IT10040': 2.9, 'IT10124': 3.3, 'IT15887': 2.1, 'IT10157': 2.5,
  'IT10133': 1.8, 'IT10127': 1.2, 'IT10178': 2.8, 'IT16850': 1.5,
  'IT15452': 1.1, 'IT10149': 1.7, 'IT10160': 0.9, 'IT16218': 0.8,
  'IT10145': 2.2, 'IT10137': 1.9, 'IT10020': 2.4,
}

// Fattore periodo: periodo più breve = CI leggermente più alto (volatilità)
const PERIOD_FACTOR = { 7: 1.12, 14: 1.06, 30: 1.0, 60: 0.95 }

export function getCiPudo(pudoId, periodGg = 30) {
  const base = CI_BASE[pudoId] ?? 0
  return +((base * (PERIOD_FACTOR[periodGg] ?? 1.0)).toFixed(2))
}

export function getCiGiro(giro, periodGg = 30) {
  const ids = [...new Set(giro.tappe.map(t => t.pudoId))]
  if (!ids.length) return 0
  return +(ids.reduce((s, id) => s + getCiPudo(id, periodGg), 0) / ids.length).toFixed(2)
}

export function getCiScenario(giriFiliale, periodGg = 30) {
  const ids = [...new Set(giriFiliale.flatMap(g => g.tappe.map(t => t.pudoId)))]
  if (!ids.length) return 0
  return +(ids.reduce((s, id) => s + getCiPudo(id, periodGg), 0) / ids.length).toFixed(2)
}

