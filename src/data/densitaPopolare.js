/**
 * Get color based on population density
 * @param {number} densita - Density value (inhabitants/km²)
 * @returns {string} Hex color code
 */
export function getDensitaColor(densita) {
  if (densita >= 3500) return '#8B0000' // Rosso scuro (densitissimo)
  if (densita >= 2500) return '#DC143C' // Rosso (molto denso)
  if (densita >= 1500) return '#FF6347' // Arancio-rosso (denso)
  if (densita >= 800) return '#FFA500' // Arancio (moderato)
  return '#FFD700' // Giallo (basso)
}

/**
 * Get color label for legend
 * @param {number} densita - Density value (inhabitants/km²)
 * @returns {string} Label for the density range
 */
export function getDensitaLabel(densita) {
  if (densita >= 3500) return 'Densitissimo'
  if (densita >= 2500) return 'Molto denso'
  if (densita >= 1500) return 'Denso'
  if (densita >= 800) return 'Moderato'
  return 'Basso'
}
